public class forloop
{
    public static void main(String[] args)
    {
        // Sum of natural numbers
        int sum= 0;
        for(int i =1;i<=10;i++ )
        {
            sum = sum + i;
        }
        System.out.println("The sum of 10 natural  number is : "+ sum);
    }
}